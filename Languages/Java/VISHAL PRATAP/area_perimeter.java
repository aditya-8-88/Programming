 public class area_perimeter
{
    public static void main (String args[])
    {
        int leng= 10,bredth= 8,area,peri;
        area= leng*bredth;
        peri= 2*(leng+bredth);
        System.out.println("Area="+area+"cm sq.");
        System.out.println("Perimeter="+peri+"cm");
    }
}
        